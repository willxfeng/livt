module ExerciseDescriptions
  SQUATS =
  "From rack with barbell upper chest height, position barbell on back of shoulders and grasp bar to sides. Dismount bar from rack.

  Squat down by bending hips back while allowing knees to bend forward, keeping back straight and knees pointed same direction as feet. Descend until knees and hips are fully bent. Extend knees and hips until legs are straight. Return and repeat."

  FRONT_SQUAT =
  "From rack with barbell upper chest height, position bar in front of shoulders. Cross arms and place hands on top of barbell with upper arms parallel to floor. Dismount bar from rack.

  Squat down by bending hips back while allowing knees to bend forward, keeping back straight and knees pointed same direction as feet. Descend until thighs are just past parallel. Extend knees and hips until legs are straight. Return and repeat."

  LEG_PRESS =
  "Sit on machine with back on padded support. Place feet on platform. Extend hips and knees. Release dock lever and grasp handles to sides.

  Lower platform by flexing hips and knees until hips are completely flexed. Return by extending knees and hips. Repeat."

  LEG_CURL =
  "Facing bench, stand between bench and lever pads. Lie prone on bench with knees just beyond edge of bench and lower legs under lever pads. Grasp handles.

  Raise lever pad to back of thighs by flexing knees. Lower lever pads until knees are straight. Repeat."

  CALF_PRESS =
  "Sit on seat with back on padded support. Place feet on platform. Grasp handles to sides and extend hips and knees. Place toes and balls of feet on lower portion of platform with heels and arches extending off.

  Push platform by extending ankles as far as possible. Return by bending ankles until calves are stretched. Repeat."

  STANDING_CALF_RAISE =
  "Place shoulders under padded lever. Position toes and balls of feet on calf block with arches and heels extending off. Grasp handles or sides of padded lever. Stand erect by extending hips and knees.

  Raise heels by extending ankles as high as possible. Lower heels by bending ankles until calves are stretched. Repeat."

  STRAIGHT_LEG_DEADLIFT =
  "Stand with shoulder width or narrower stance on shallow platform with feet flat beneath bar. Bend knees and bend over with lower back straight. Grasp barbell with shoulder width overhand or mixed grip, shoulder width or slightly wider. Lift weight to standing position.

  With knees straight, lower bar toward top of feet by bending hips. After hips can no longer flex, bend waist as bar approaches top of feet. Lift bar by extending waist and hip until standing upright. Pull shoulders back slightly if rounded. Repeat."

  BARBELL_CURL =
  "Grasp bar with shoulder width underhand grip.

  With elbows to side, raise bar until forearms are vertical. Lower until arms are fully extended. Repeat."

  DUMBELL_CURL =
  "Position two dumbbells to sides, palms facing in, arms straight.

  With elbows to sides, raise one dumbbell and rotate forearm until forearm is vertical and palm faces shoulder. Lower to original position and repeat with opposite arm. Continue to alternate between sides."

  SEATED_CURL =
  "Sit back on 45-60 degree incline bench. With arms hanging down straight, position two dumbbells with palms facing inward.

  With elbows back to sides, raise one dumbbell and rotate forearm until forearm is vertical and palm faces shoulder. Lower to original position and repeat with opposite arm. Continue to alternate between sides."

  CABLE_CURL =
  "Grasp low pulley cable bar with shoulder width underhand grip. Stand close to pulley.

  With elbows to side, raise bar until forearms are vertical. Lower until arms are fully extended. Repeat."

  CLOSE_GRIP_BENCH =
  "Lie on bench and grasp barbell from rack with shoulder width grip.

  Lower weight to chest with elbows close to body. Push barbell back up until arms are straight. Repeat."

  LYING_TRICEP_EXTENSION =
  "Lie on bench with narrow overhand grip on barbell. Position barbell over forehead with arms extended.

  Lower bar by bending elbows. As bar nears head, move elbows slightly back just enough to allow bar to clear around curvature of head. Extend arms. As bar clears head, reposition elbows to their former position until arms are fully extended. Repeat."

  
