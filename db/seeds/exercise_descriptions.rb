module ExerciseDescriptions
  SQUAT =
  "From rack with barbell upper chest height, position barbell on back of shoulders and grasp bar to sides. Dismount bar from rack.

  Squat down by bending hips back while allowing knees to bend forward, keeping back straight and knees pointed same direction as feet. Descend until knees and hips are fully bent. Extend knees and hips until legs are straight. Return and repeat."

  FRONT_SQUAT =
  "From rack with barbell upper chest height, position bar in front of shoulders. Cross arms and place hands on top of barbell with upper arms parallel to floor. Dismount bar from rack.

  Squat down by bending hips back while allowing knees to bend forward, keeping back straight and knees pointed same direction as feet. Descend until thighs are just past parallel. Extend knees and hips until legs are straight. Return and repeat."

  LEG_PRESS =
  "Sit on machine with back on padded support. Place feet on platform. Extend hips and knees. Release dock lever and grasp handles to sides.

  Lower platform by flexing hips and knees until hips are completely flexed. Return by extending knees and hips. Repeat."

  LEG_CURL =
  "Facing bench, stand between bench and lever pads. Lie prone on bench with knees just beyond edge of bench and lower legs under lever pads. Grasp handles.

  Raise lever pad to back of thighs by flexing knees. Lower lever pads until knees are straight. Repeat."

  CALF_PRESS =
  "Sit on seat with back on padded support. Place feet on platform. Grasp handles to sides and extend hips and knees. Place toes and balls of feet on lower portion of platform with heels and arches extending off.

  Push platform by extending ankles as far as possible. Return by bending ankles until calves are stretched. Repeat."

  STANDING_CALF_RAISE =
  "Place shoulders under padded lever. Position toes and balls of feet on calf block with arches and heels extending off. Grasp handles or sides of padded lever. Stand erect by extending hips and knees.

  Raise heels by extending ankles as high as possible. Lower heels by bending ankles until calves are stretched. Repeat."

  STRAIGHT_LEG_DEADLIFT =
  "Stand with shoulder width or narrower stance on shallow platform with feet flat beneath bar. Bend knees and bend over with lower back straight. Grasp barbell with shoulder width overhand or mixed grip, shoulder width or slightly wider. Lift weight to standing position.

  With knees straight, lower bar toward top of feet by bending hips. After hips can no longer flex, bend waist as bar approaches top of feet. Lift bar by extending waist and hip until standing upright. Pull shoulders back slightly if rounded. Repeat."

  BARBELL_CURL =
  "Grasp bar with shoulder width underhand grip.

  With elbows to side, raise bar until forearms are vertical. Lower until arms are fully extended. Repeat."

  DUMBBELL_CURL =
  "Position two dumbbells to sides, palms facing in, arms straight.

  With elbows to sides, raise one dumbbell and rotate forearm until forearm is vertical and palm faces shoulder. Lower to original position and repeat with opposite arm. Continue to alternate between sides."

  HAMMER_CURL =
  "Position two dumbbells to sides, palms facing in, arms straight.

  With elbows to sides, raise one dumbbell until forearm is vertical and thumb faces shoulder. Lower to original position and repeat with alternative arm."

  SEATED_CURL =
  "Sit back on 45-60 degree incline bench. With arms hanging down straight, position two dumbbells with palms facing inward.

  With elbows back to sides, raise one dumbbell and rotate forearm until forearm is vertical and palm faces shoulder. Lower to original position and repeat with opposite arm. Continue to alternate between sides."

  CABLE_CURL =
  "Grasp low pulley cable bar with shoulder width underhand grip. Stand close to pulley.

  With elbows to side, raise bar until forearms are vertical. Lower until arms are fully extended. Repeat."

  CLOSE_GRIP_BENCH_PRESS =
  "Lie on bench and grasp barbell from rack with shoulder width grip.

  Lower weight to chest with elbows close to body. Push barbell back up until arms are straight. Repeat."

  LYING_TRICEP_EXTENSION =
  "Lie on bench with narrow overhand grip on barbell. Position barbell over forehead with arms extended.

  Lower bar by bending elbows. As bar nears head, move elbows slightly back just enough to allow bar to clear around curvature of head. Extend arms. As bar clears head, reposition elbows to their former position until arms are fully extended. Repeat."

  CABLE_PUSHDOWN =
  "Face high pulley and grasp cable attachment with narrow overhand grip. Position elbows to side.

  Extend arms down. Return until forearm is close to upper arm. Repeat."

  TRICEP_DIP =
  "Mount shoulder width dip bar, arms straight with shoulders above hands. Keep hips straight.

  Lower body until slight stretch is felt in shoulders. Push body up until arms are straight. Repeat."

  ONE_ARM_TRICEP_EXTENSION =
  "Sit on seat with back support just below shoulder height. Position dumbbell over head with arm straight up or slightly back.

  Lower dumbbell behind neck or shoulder while maintaining upper arm's vertical position throughout exercise. Extend arm until straight. Return and repeat. Continue with opposite arm."

  DUMBBELL_WRIST_CURL =
  "Sit and grasp dumbbell with underhand grip. Rest forearm on thigh with wrist just beyond knee.

  Allow dumbbell to roll out of palm down to fingers. Raise dumbbell back up by gripping and pointing knuckles up as high as possible. Lower and repeat."

  MILITARY_PRESS =
  "Grasp barbell from rack or clean barbell from floor with overhand grip, slightly wider than shoulder width. Position bar in front of neck.

  Grasp barbell from rack or clean barbell from floor with overhand grip, slightly wider than shoulder width. Position bar in front of neck."

  ARNOLD_PRESS =
  "Stand with two dumbbells position in front of shoulders, palms facing body and elbows under wrists.

  Initiate movement by bringing elbows out to sides. Continue to raise elbows outward while pressing dumbbells overhead until arms are straight. Lower to front of shoulders in opposite pattern and repeat."

  DUMBBELL_SHOULDER_PRESS =
  "Position dumbbells to each side of shoulders with elbows below wrists.

  Press dumbbells upward until arms are extended overhead. Lower to sides of shoulders and repeat."

  LATERAL_RAISE =
  "Grasp dumbbells in front of thighs with elbows slightly bent. Bend over slightly with hips and knees bent slightly.

  Raise upper arms to sides until elbows are shoulder height. Maintain elbows' height above or equal to wrists. Lower and repeat."

  UPRIGHT_ROW =
  "Grasp bar with shoulder width or slightly narrower overhand grip.

  Pull bar to neck with elbows leading. Allow wrists to flex as bar rises. Lower and repeat."

  BARBELL_SHRUG =
  "Stand holding barbell with overhand or mixed grip; shoulder width or slightly wider.

  Elevate shoulders as high as possible. Lower and repeat."

  DUMBBELL_SHRUG =
  "Stand holding dumbbells to sides.

  Elevate shoulders as high as possible. Lower and repeat."

  DEADLIFT =
  "With feet flat beneath bar squat down and grasp bar with shoulder width or slightly wider overhand or mixed grip.

  Lift bar by extending hips and knees to full extension. Pull shoulders back at top of lift if rounded. Return weights to floor by bending hips back while allowing knees to bend forward, keeping back straight and knees pointed same direction as feet. Repeat."

  CABLE_PULLDOWN =
  "Grasp cable bar with wide grip. Sit with thighs under supports.

  Pull down cable bar to upper chest. Return until arms and shoulders are fully extended. Repeat."

  PULL_UP =
  "Step up and grasp bar with overhand wide grip.

  Pull body up until chin is above bar. Lower body until arms and shoulders are fully extended. Repeat."

  BENT_OVER_ROW =
  "Bend knees slightly and bend over bar with back straight. Grasp bar with wide overhand grip.

  Pull bar to upper waist. Return until arms are extended and shoulders are stretched downward. Repeat."

  SEATED_CABLE_ROW =
  "Sit slightly forward on bench with feet on foot bar or vertical platform. Grasp close grip cable attachment. Straighten torso upright and slide hips back so knees are slightly bent.

  Pull cable attachment to waist. Pull shoulders back and lift chest by arching back. Return until arms are extended, back is straight, and shoulders are stretched forward. Repeat."

  GOOD_MORNING =
  "Position barbell on back of shoulders and grasp bar to sides.

  Keeping back straight, bend hips to lower torso forward until parallel to floor. Raise torso until hips are extended. Repeat."

  HYPEREXTENSION =
  "Position thighs prone on large pad and lower legs under padded brace. Place hands behind neck or to sides of head.

  Raise upper body until hips and waist are fully extended. Lower body by bending hips and waist until fully flexed. Repeat."

  BARBELL_BENCH_PRESS =
  "Lie supine on bench. Dismount barbell from rack over upper chest using wide oblique overhand grip.

  Lower weight to chest. Press bar upward until arms are extended. Repeat."

  BARBELL_INCLINE_BENCH_PRESS =
  "Lie supine on incline bench. Dismount barbell from rack over upper chest using wide oblique overhand grip.

  Lower weight to upper chest. Press bar until arms are extended. Repeat."

  BARBELL_DECLINE_BENCH_PRESS =
  "Lie supine on decline bench with feet under leg brace. Dismount barbell from rack over chest using wide oblique overhand grip.

  Lower weight to chest. Press bar until arms are extended. Repeat."

  DUMBBELL_BENCH_PRESS =
  "Sit down on bench with dumbbells resting on lower thigh. Kick weights to shoulder and lie back. Position dumbbells to sides of chest with bent arm under each dumbbell.

  Press dumbbells up with elbows to sides until arms are extended. Lower weight to sides of chest until slight stretch is felt in chest or shoulder. Repeat."

  DUMBBELL_INCLINE_BENCH_PRESS =
  "Sit down on incline bench with dumbbells resting on lower thigh. Kick weights to shoulders and lean back. Position dumbbells to sides of chest with upper arm under each dumbbell.

  Press dumbbells up with elbows to sides until arms are extended. Lower weight to sides of upper chest until slight stretch is felt in chest or shoulder. Repeat."

  DUMBBELL_FLY =
  "Grasp two dumbbells. Lie supine on bench. Support dumbbells above chest with arms fixed in slightly bent position. Internally rotate shoulders so elbows point out to sides.

  Lower dumbbells to sides until chest muscles are stretched with elbows fixed in slightly bent position. Bring dumbbells together in wide hugging motion until dumbbells are nearly together. Repeat."

  CHEST_DIP =
  "Mount wide dip bar with oblique grip (bar diagonal under palm), arms straight with shoulders above hands. Bend knees and hips slightly.

  Lower body by bending arms, allowing elbows to flare out to sides. When slight stretch is felt in chest or shoulders, push body up until arms are straight. Repeat."

  INCLINE_CRUNCH =
  "Hook feet under foot brace and lie supine on incline board with hips bent. Place hands behind neck or head.

  Flex waist to raise upper torso from bench. Keep low back on bench and raise torso up as high as possible. Return until back of shoulders contact incline board. Repeat."

  CABLE_KNEELING_CRUNCH =
  "Kneel below high pulley. Grasp cable rope attachment with both hands. Place wrists against head. Position hips back and flex hips, allowing resistance on cable pulley to lift torso upward so spine is hyperextended.

  With hips stationary, flex waist so elbows travel toward middle of thighs. Return and repeat."

  LEG_RAISE =
  "Lie supine on bench or mat. Place hands under lower buttock on each side to support pelvis.

  Keeping knees straight, raise legs by flexing hips until hips are completely flexed. Return until hips and knees are extended. Repeat."

  OVERHEAD_SQUAT =
  "Snatch or Hang Snatch barbell over head with very wide overhand grip. Position toes outward with wide stance. Maintain bar behind head with arms extended.

  Descend until knees and hips are fully bent or until thighs are just past parallel to floor. Knees travel in direction of toes. Extend knees and hips until legs are straight. Return and repeat."

  MUSCLE_UP =
  "Jump up and grasp high vertical bar with shoulder width or slightly wider grip. Grip higher around bar with wrists flexed and palm positioned over bar

  Pull just before body swings backwards, forcefully pull body up behind bar and throw knees up for momentum. As elbows clear height of bar from behind, lean over bar and flex spine while keeping knee lifted in front of bar from below. Push as soon as elbows are above bar, extend arms and knees straight while maintaining slight bend in hip."
